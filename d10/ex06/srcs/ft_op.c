int	ft_mo(int a, int b)
{
	return (a % b);
}

int	ft_di(int a, int b)
{
	return (a / b);
}

int	ft_ad(int a, int b)
{
	return (a + b);
}

int	ft_su(int a, int b)
{
	return (a - b);
}

int	ft_mu(int a, int b)
{
	return (a * b);
}
