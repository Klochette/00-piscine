char	*ft_strrev(char *str)
{
	int i;
	int j;
	int k;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	j = -1;
	while (j < i)
	{
		k = str[j];
		str[j] = str[i];
		str[i] = k;
		j++;
		i--;
	}
	return (str);
}
